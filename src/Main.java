
public class Main {
	public static void main(String[] args) {
		System.out.println("Clonar repositório");
		System.out.println("git clone");
		System.out.println("==========");
		System.out.println("Criar branch e alterar para a mesma");
		System.out.println("git branch -b nomeDaBranch");
		System.out.println("==========");
		System.out.println("Adicionar mudanças feitas");
		System.out.println("git add .");
		System.out.println("==========");
		System.out.println("Commitar mudanças realizadas");
		System.out.println("git commit -m mensagemDoCommit");
		System.out.println("==========");
		System.out.println("Enviar para o repositorio remoto");
		System.out.println("git push origin master");
		System.out.println("==========");
	}

}
